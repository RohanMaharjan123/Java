package Ch2;
public class datatype {
    public static void main(String[] args){
        // Primitive DataTypes
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + 6;
        System.out.println(b);
        System.out.println(a);
        System.out.println(x);

        //Increment and Decrement Operators
        int i = 50;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int m = 7;
        System.out.println(++m *8);
    }
}
